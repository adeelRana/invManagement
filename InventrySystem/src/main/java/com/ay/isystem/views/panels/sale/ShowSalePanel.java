    private void loadDataToTable() {
        dtm.setRowCount(0);
        ArrayList<Deals> li = DBM.getRecordsBy(Deals.class, "TransactionTypeId", 5);
        for (Deals d : li) {
            dtm.addRow(new Object[]{
                d,
                Global.DATE_FORMAT.format(new Date(d.getInvoiceDate())),
                d.getParty(),
                d.getTransactionAmount(),
                d.getAgentId() == 0 ? "NO" : "YES",
                d.getReturnDetails().isEmpty() ? "NO" : "YES"
            });
        }
    }
