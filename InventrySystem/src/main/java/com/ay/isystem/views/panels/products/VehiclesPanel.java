    private void loadDataToTable() {
        dtm.setRowCount(0);
        ArrayList<Vehicle> li = DBM.getAllRecords(Vehicle.class);
        for (Vehicle v : li) {
            dtm.addRow(new Object[]{
                v, v.getModel()
            });
        }
    }
