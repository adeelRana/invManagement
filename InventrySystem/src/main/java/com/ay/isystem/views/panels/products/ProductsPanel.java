    private void loadDataToTable() {
        double grandTotal = 0;
        dtm.setRowCount(0);
        ArrayList<Products> li = DBM.getAllRecords(Products.class);
        Vehicle v;
        for (Products p : li) {
            grandTotal += p.getPrice() * p.getQuantity();
            dtm.addRow(new Object[]{
                p, p.getName(), p.getVehiclesList(), p.getQuantity(), p.getPrice(), (p.getQuantity() * p.getPrice()), p.getRemarks()
            });
        }
        grandTotalTxt.setText(new DecimalFormat("#.##").format(grandTotal));
    }
