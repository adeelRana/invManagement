    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTable1.getSelectedRowCount() > 0 && jTable1.getSelectedRowCount() < 2) {
            AddProductsDialog apd = new AddProductsDialog(null, true, (Products) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            apd.setVisible(true);
            loadDataToTable();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRowCount() > 0 && jTable1.getSelectedRowCount() < 2) {
            int row = jTable1.getSelectedRow();
            Products product = (Products) jTable1.getValueAt(row, 0);
            for (ProductsVehicles v : (ArrayList<ProductsVehicles>) jTable1.getValueAt(row, 2)) {
                Global.CONTROLLER.deleteRecord(v.getId(), ProductsVehicles.class);
            }
            Response deleteRecord = Global.CONTROLLER.deleteRecord(product.getId(), Products.class);
            if (!deleteRecord.isError()) {
                dtm.removeRow(row);
            } else {
                JOptionPane.showMessageDialog(this, deleteRecord.getErrorMsg());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select One record");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


