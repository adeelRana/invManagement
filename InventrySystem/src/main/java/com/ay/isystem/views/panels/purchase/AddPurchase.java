    private void btnSave2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave2ActionPerformed
        if (tblItems.getRowCount() > 0) {
            Deals purchase = new Deals();
            try {
                purchase.setSaleReturn(isPurchaseReturn);
                purchase.setUpdate(isUpdate);
                purchase.setInvoiceDate(jXDatePicker1.getDate().getTime());
                purchase.setInvoiceNo(Utility.getSaleInvoiceNo(jXDatePicker1.getDate()));
                purchase.setParty(((Vendors) comboCustomerNamesAuto.getSelectedItem()));
                purchase.setPartyId(((Vendors) comboCustomerNamesAuto.getSelectedItem()).getId());
                purchase.setTransactionAmount(Double.parseDouble(txtInvoiceTotal.getText()));
                purchase.setTransactionTypeId(TransactionType.PURCHASE);
                purchase.setAgentId(0);
                DealDetails dd;
                ArrayList<DealDetails> detalisList = new ArrayList<>();
                Products p;
                for (int row = 0; row < tblItems.getRowCount(); row++) {
                    dd = new DealDetails();
                    p = (Products) tblItems.getValueAt(row, 0);
                    dd.setProductId(p.getId());
                    dd.setProductQty(Double.parseDouble(tblItems.getValueAt(row, 1).toString()));
                    dd.setTansactionDate(jXDatePicker1.getDate().getTime());
                    dd.setProductInvoiceRate(Double.parseDouble(tblItems.getValueAt(row, 3).toString()));
                    dd.setProductSaleRate(Double.parseDouble(tblItems.getValueAt(row, 2).toString()));
                    dd.setProductInvoiceRate(Double.parseDouble(tblItems.getValueAt(row, 2).toString()));
                    dd.setTransactionTypeId(TransactionType.PURCHASE);
                    detalisList.add(dd);
                }
                purchase.setDetails(detalisList);
                Global.CONTROLLER.addDeal(purchase, TransactionType.PURCHASE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            Global.addPanel(ShowPurchase.class, (JPanel) this.getParent());

        }

    }//GEN-LAST:event_btnSave2ActionPerformed

