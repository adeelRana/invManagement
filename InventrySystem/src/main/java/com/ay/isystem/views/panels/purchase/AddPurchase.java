    private void tableAutoTotal() {
        double quantity;
        double invoiceRate;
        double totalInvoiceSale = 0;

        for (int rowRight = 0; rowRight < tblItems.getRowCount(); rowRight++) {
            quantity = 0;
            invoiceRate = 0;
            try {
                quantity = Double.parseDouble(tblItems.getValueAt(rowRight, 1).toString());
                invoiceRate = Double.parseDouble(tblItems.getValueAt(rowRight, 2).toString());

                totalInvoiceSale += quantity * invoiceRate;
            } catch (NumberFormatException ex) {
            }
            tblItems.setValueAt(quantity * invoiceRate, rowRight, 3);
        }
        txtInvoiceTotal.setText(totalInvoiceSale + "");

    }

