    private void addDataToTable() {

        ArrayList<Users> list = (ArrayList<Users>) Global.CONTROLLER.fetch("users").getResponseObject();
        for (Users u : list) {
            dtm.addRow(new Object[]{
                u,
                u.getType(),
                Global.DATE_FORMAT.format(new Date(u.getLastLoginTime()))
            });
        }
    }
